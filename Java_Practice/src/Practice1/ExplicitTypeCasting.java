package Practice1;

public class ExplicitTypeCasting {

	public static void main(String[] args) {
		// explicit type casting is to convert higher order data type to lower order data type
		double f = 10.52f;
		long l = (long)f;
		System.out.println(l);
		
		float f1 = 34.56f;
		int i = (int)f1;
		System.out.println(i);
		
	}

}
