package Practice1;

public class ImplicitTypeCasting {

	public static void main(String[] args) {
		//implicit type casting is to convert lower order data type to higher order data type
		byte b = 10;
		int i = b;
		System.out.println(i);
		
		float f = 22.14f;
		double d = f;
		System.out.println(d);
		
		char ch = 'A';
		int i1 = ch;
		System.out.println(i1);
		

	}

}
