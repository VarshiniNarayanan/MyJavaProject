package Practice1;

public class NumericalData {

	public static void main(String[] args) {
		// description of division of diff data types
		int x1 = 5/2;// integer division - returns integer
		System.out.println("the value of x1 is "+ x1);
		float x2 = 43/7; // integer division - returns float
		System.out.println("the value of x2 is "+ x2);
		float x3 = 6/2.5f;//float division - returns float
		System.out.println("the value of x3 is "+ x3);
		double x4 = 10d/6d;//double division -return double
		System.out.println("the value of x4 is "+ x4);
		//using expression
		int x = 32;
		double percent = x * 0.46f;
		System.out.println(percent);
	}

}
