package Practice1;

public class CharData {

	public static void main(String[] args) {
		//single character
		char character = 'x';
		System.out.println(character);
		//number character
		char num = 65;
		System.out.println("the character is " + num);
		//number is converted into ascii
		//unicode
		char code1 = '\u20AC';
		System.out.println(code1);
		// unicode for euro symbol
	}

}
